package com.bvan.javastart.lessons3_4.types;

/**
 * @author bvanchuhov
 */
public class AllTypes {

    public static void main(String[] args) {
        // Integer
        byte b = 127; // 1B
        short s = 10; // 2B
        int x = 2_000_000_000; // 4B
        long l = 1234567890123456789L; // 8B

        // Floating point
        float f = 10.5f; // 4B
        double d = 10.5; // 8B

        // Char
        char c = 'a'; // 2B

        // Logical
        boolean bool = true;
    }
}
